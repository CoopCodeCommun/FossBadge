# ============================================================================
# Fonction utilitaire : construire un polygone arrondi.
# On calcule les sommets du polygone, puis on les relie
# par des courbes de bezier pour arrondir les coins.
#
# Utility: build a rounded polygon.
# Compute polygon vertices, connect them with bezier curves.
# ============================================================================

def _build_rounded_polygon_path(
    number_of_sides,
    radius,
    tension=0.35,
    center_x=CENTER_X,
    center_y=CENTER_Y,
):
    """
    On fabrique un polygone regulier avec des coins arrondis.
    Le premier sommet est en haut (a midi).

    Build a regular polygon with rounded corners.
    First vertex is at the top (12 o'clock).
    """

    # On calcule tous les sommets du polygone.
    # Compute all polygon vertices.
    all_vertices = []

    for vertex_index in range(number_of_sides):
        # L'angle de chaque sommet. On commence en haut (-90 degres).
        # Angle for each vertex, starting from top (-90 degrees).
        angle_in_radians = (
            2 * math.pi * vertex_index / number_of_sides
        ) - (math.pi / 2)

        vertex_x = center_x + radius * math.cos(angle_in_radians)
        vertex_y = center_y + radius * math.sin(angle_in_radians)

        # Direction tangente (perpendiculaire au rayon, sens horaire).
        # Tangent direction (perpendicular to radius, clockwise).
        tangent_x = -math.sin(angle_in_radians)
        tangent_y = math.cos(angle_in_radians)

        all_vertices.append({
            "x": vertex_x,
            "y": vertex_y,
            "tx": tangent_x,
            "ty": tangent_y,
        })

    # On construit le chemin SVG avec des courbes de bezier.
    # Build SVG path with bezier curves.
    path_parts = []
    first_vertex = all_vertices[0]
    path_parts.append(f"M{first_vertex['x']:.1f} {first_vertex['y']:.1f}")

    for vertex_index in range(number_of_sides):
        current_vertex = all_vertices[vertex_index]
        next_vertex = all_vertices[(vertex_index + 1) % number_of_sides]

        delta_x = next_vertex["x"] - current_vertex["x"]
        delta_y = next_vertex["y"] - current_vertex["y"]
        distance = math.sqrt(delta_x * delta_x + delta_y * delta_y)
        arm_length = distance * tension

        cp1_x = current_vertex["x"] + current_vertex["tx"] * arm_length
        cp1_y = current_vertex["y"] + current_vertex["ty"] * arm_length

        cp2_x = next_vertex["x"] - next_vertex["tx"] * arm_length
        cp2_y = next_vertex["y"] - next_vertex["ty"] * arm_length

        path_parts.append(
            f"C{cp1_x:.1f} {cp1_y:.1f} "
            f"{cp2_x:.1f} {cp2_y:.1f} "
            f"{next_vertex['x']:.1f} {next_vertex['y']:.1f}"
        )

    path_parts.append("Z")
    return " ".join(path_parts)


# ============================================================================
# Fonction utilitaire : construire une etoile arrondie.
# Les etoiles ont des pointes (rayon exterieur) et des creux (rayon interieur).
# On les relie par des courbes de bezier pour un rendu doux.
#
# Utility: build a rounded star.
# Stars have peaks (outer radius) and valleys (inner radius).
# Connected with bezier curves for a smooth look.
# ============================================================================

def _build_rounded_star_path(
    number_of_peaks,
    outer_radius,
    inner_radius,
    tension=0.38,
    center_x=CENTER_X,
    center_y=CENTER_Y,
):
    """
    On fabrique une etoile avec des pointes arrondies.
    La premiere pointe est en haut.

    Build a star with rounded peaks. First peak is at top.
    """

    # On calcule tous les points : pointe, creux, pointe, creux...
    # Compute all points: peak, valley, peak, valley...
    total_points = number_of_peaks * 2
    all_points = []

    for point_index in range(total_points):
        angle = (2 * math.pi * point_index / total_points) - (math.pi / 2)
        is_peak = (point_index % 2 == 0)
        radius = outer_radius if is_peak else inner_radius

        point_x = center_x + radius * math.cos(angle)
        point_y = center_y + radius * math.sin(angle)
        tangent_x = -math.sin(angle)
        tangent_y = math.cos(angle)

        all_points.append({
            "x": point_x,
            "y": point_y,
            "tx": tangent_x,
            "ty": tangent_y,
        })

    # On construit le chemin SVG avec des courbes de bezier.
    # Build SVG path with bezier curves.
    path_parts = []
    first_point = all_points[0]
    path_parts.append(f"M{first_point['x']:.1f} {first_point['y']:.1f}")

    for point_index in range(total_points):
        current = all_points[point_index]
        next_point = all_points[(point_index + 1) % total_points]

        delta_x = next_point["x"] - current["x"]
        delta_y = next_point["y"] - current["y"]
        distance = math.sqrt(delta_x * delta_x + delta_y * delta_y)
        arm_length = distance * tension

        cp1_x = current["x"] + current["tx"] * arm_length
        cp1_y = current["y"] + current["ty"] * arm_length
        cp2_x = next_point["x"] - next_point["tx"] * arm_length
        cp2_y = next_point["y"] - next_point["ty"] * arm_length

        path_parts.append(
            f"C{cp1_x:.1f} {cp1_y:.1f} "
            f"{cp2_x:.1f} {cp2_y:.1f} "
            f"{next_point['x']:.1f} {next_point['y']:.1f}"
        )

    path_parts.append("Z")
    return " ".join(path_parts)


# ============================================================================
# Forme : Cercle.
# Le cercle est la forme la plus simple. C'est la forme du Savoir-Vivre (Sv)
# dans Le Dome.
#
# Shape: Circle. Simplest shape. Used by Savoir-Vivre (Sv) in Le Dome.
# ============================================================================

def _build_circle_path(radius=230, center_x=CENTER_X, center_y=CENTER_Y):
    """
    On fabrique un cercle parfait en SVG.
    On utilise 2 arcs de cercle qui se rejoignent.

    Build a perfect SVG circle using 2 arcs.
    """
    top_y = center_y - radius
    bottom_y = center_y + radius

    return (
        f"M{center_x:.1f} {top_y:.1f} "
        f"A{radius} {radius} 0 1 1 {center_x:.1f} {bottom_y:.1f} "
        f"A{radius} {radius} 0 1 1 {center_x:.1f} {top_y:.1f} Z"
    )


# ============================================================================
# Forme : Bouclier / Ecusson.
# Un ecusson classique avec un haut arrondi et un bas en pointe.
# C'est une forme courante pour les badges et les blasons.
#
# Shape: Shield / Crest.
# Classic shield with rounded top and pointed bottom.
# ============================================================================

def _build_shield_path(center_x=CENTER_X, center_y=CENTER_Y):
    """
    On fabrique un ecusson en bezier.
    Le haut est arrondi, le bas se termine en pointe.

    Build a shield shape with bezier curves.
    Rounded top, pointed bottom.
    """
    # Dimensions de l'ecusson.
    # Shield dimensions.
    half_width = 200
    top_y = center_y - 210
    bottom_y = center_y + 230
    shoulder_y = center_y - 120
    waist_y = center_y + 60

    left_x = center_x - half_width
    right_x = center_x + half_width

    return (
        f"M{left_x:.1f} {shoulder_y:.1f} "
        f"L{left_x:.1f} {top_y:.1f} "
        f"Q{left_x:.1f} {top_y - 50:.1f} {center_x:.1f} {top_y - 50:.1f} "
        f"Q{right_x:.1f} {top_y - 50:.1f} {right_x:.1f} {top_y:.1f} "
        f"L{right_x:.1f} {shoulder_y:.1f} "
        f"L{right_x:.1f} {waist_y:.1f} "
        f"Q{right_x:.1f} {bottom_y - 40:.1f} {center_x:.1f} {bottom_y:.1f} "
        f"Q{left_x:.1f} {bottom_y - 40:.1f} {left_x:.1f} {waist_y:.1f} "
        f"Z"
    )


