# Formes du Dome.
# Le generateur du Dome (openbadges.ledome.info) utilise 8 formes distinctes,
# une par categorie de badge : CP, SV, SF, SE, XP, PJ, GP, PC.
# Chaque forme est definie par un chemin SVG dans un viewBox specifique.
# On les normalise toutes vers notre espace 500x500.
#
# Dome shapes.
# The Dome generator uses 8 distinct shapes, one per badge category.
# Each is defined in its own viewBox. We normalize them to 500x500.
# ============================================================================


def _rescale_dome_path(
    path_d,
    viewbox_size,
    group_translate_x=0,
    group_translate_y=0,
):
    """
    On normalise un chemin SVG du Dome vers notre espace 500x500.
    Le Dome utilise des viewBox de tailles differentes (840, 850, 888, 1247).
    On redimensionne toutes les coordonnees pour que le badge s'affiche
    correctement dans un carre de 500x500 pixels.

    Les coordonnees absolues (commandes majuscules) sont translatees puis mises a l'echelle.
    Les coordonnees relatives (commandes minuscules) sont seulement mises a l'echelle.
    Exception : le premier "m" est traite comme absolu (spec SVG).

    Normalize a Dome SVG path to our 500x500 coordinate space.
    Absolute coords (uppercase commands) get translated then scaled.
    Relative coords (lowercase commands) get scaled only.
    Exception: first "m" is treated as absolute (SVG spec).
    """

    target_size = 500
    scale = target_size / viewbox_size

    # On decoupe le chemin en tokens : lettres de commande et nombres.
    # Tokenize path into command letters and numbers.
    token_pattern = (
        r'[MmCcLlHhVvZz]'
        r'|[-+]?(?:\d+\.?\d*|\.\d+)(?:[eE][-+]?\d+)?'
    )
    tokens = re.findall(token_pattern, path_d)

    result_parts = []
    index = 0
    current_command = None
    is_first_moveto = True

    while index < len(tokens):
        token = tokens[index]

        # Si c'est Z ou z, on le garde tel quel (fermer le chemin).
        # Z/z closes the path, no coordinates.
        if token in ('Z', 'z'):
            result_parts.append(token)
            index += 1
            continue

        # Si c'est une lettre de commande, on la garde et on avance.
        # If it's a command letter, store it and advance.
        if token.isalpha():
            current_command = token
            result_parts.append(token)
            index += 1
            continue

        # C'est un nombre. On le traite selon la commande en cours.
        # It's a number. Process based on current command.

        # Le premier "m" est traite comme un "M" absolu (spec SVG).
        # First "m" is treated as absolute "M" (SVG spec).
        if current_command == 'm' and is_first_moveto:
            treat_as_absolute = True
            is_first_moveto = False
        elif current_command and current_command.isupper():
            treat_as_absolute = True
        else:
            treat_as_absolute = False

        command_upper = current_command.upper() if current_command else 'M'

        # --- Commandes avec 2 coordonnees (x, y) ---
        # Commands with 2 coordinates (x, y): M, L
        if command_upper in ('M', 'L'):
            x_value = float(tokens[index])
            y_value = float(tokens[index + 1])
            if treat_as_absolute:
                x_value = (x_value + group_translate_x) * scale
                y_value = (y_value + group_translate_y) * scale
            else:
                x_value = x_value * scale
                y_value = y_value * scale
            result_parts.append(f'{x_value:.3f},{y_value:.3f}')
            index += 2

        # --- Courbe cubique de Bezier (6 coordonnees) ---
        # Cubic bezier curve (6 coordinates): C
        elif command_upper == 'C':
            for _bezier_point in range(3):
                x_value = float(tokens[index])
                y_value = float(tokens[index + 1])
                if treat_as_absolute:
                    x_value = (x_value + group_translate_x) * scale
                    y_value = (y_value + group_translate_y) * scale
                else:
                    x_value = x_value * scale
                    y_value = y_value * scale
                result_parts.append(f'{x_value:.3f},{y_value:.3f}')
                index += 2

        # --- Ligne horizontale (1 coordonnee x) ---
        # Horizontal line (1 x coordinate): H
        elif command_upper == 'H':
            x_value = float(tokens[index])
            if treat_as_absolute:
                x_value = (x_value + group_translate_x) * scale
            else:
                x_value = x_value * scale
            result_parts.append(f'{x_value:.3f}')
            index += 1

        # --- Ligne verticale (1 coordonnee y) ---
        # Vertical line (1 y coordinate): V
        elif command_upper == 'V':
            y_value = float(tokens[index])
            if treat_as_absolute:
                y_value = (y_value + group_translate_y) * scale
            else:
                y_value = y_value * scale
            result_parts.append(f'{y_value:.3f}')
            index += 1

        # --- Commande non geree, on passe le nombre tel quel. ---
        # Unhandled command, pass number as-is.
        else:
            result_parts.append(token)
            index += 1

    return ' '.join(result_parts)


